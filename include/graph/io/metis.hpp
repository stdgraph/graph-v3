/**
 * @file metis.hpp
 * @brief METIS graph I/O — write and read.
 *
 * Provides:
 *   - write_metis(os, g)        Emit a graph in METIS adjacency format
 *   - read_metis(is)            Parse a METIS file into metis_graph
 *
 * The METIS graph format describes an *undirected* graph:
 *
 *   % comment line (ignored)
 *   <n> <m> [fmt] [ncon]                 header: vertices, edges, format flags
 *   <adjacency of vertex 1>              one line per vertex (1-indexed)
 *   <adjacency of vertex 2>
 *   ...
 *
 * `m` counts each undirected edge once.  The optional 3-digit `fmt` flag
 * encodes whether vertex sizes / vertex weights / edge weights are present
 * (hundreds = vertex sizes, tens = vertex weights, ones = edge weights).
 * When edge weights are present, each neighbour is followed by its weight.
 *
 * The writer treats the input graph as undirected: each edge (u,v) contributes
 * v to u's list and u to v's list, deduplicated, so the output is symmetric.
 * The reader normalizes neighbour ids from the file's 1-indexed convention to
 * 0-indexed ids.
 *
 * Reference: METIS manual, "Graph Input File" section.
 *
 * NOTE: Self-contained — no external dependencies.
 */

